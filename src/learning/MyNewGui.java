	private void CheckAnswer () {
		String CAnswer = new String(CountryName1.getText());
		if (!CAnswer.equals("")) {
			if (CAnswer.toLowerCase().equals(currentQuestion.answer)) {
				JOptionPane.showMessageDialog(this, "Good answer!", "Result", JOptionPane.WARNING_MESSAGE);
			} else {
				JOptionPane.showMessageDialog(this, "Wrong answer", "Result", JOptionPane.WARNING_MESSAGE);
			}
			currentQuestion = questions[getRandomQuestion()];
			drawFlag(currentQuestion);
			CountryName1.setText("");
		} else {
			JOptionPane.showMessageDialog(this, "You did not write anything.", "Empty answer warning", JOptionPane.WARNING_MESSAGE);
		}
	}
