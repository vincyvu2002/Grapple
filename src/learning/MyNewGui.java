	private void drawFlag(Question question) {
		BufferedImage resizedImage = new BufferedImage(200, 100, BufferedImage.TYPE_INT_RGB);
		Graphics graf = resizedImage.getGraphics();
		graf.drawImage(question.flag, 0, 0, 200, 100, null);
		flag.setIcon(new ImageIcon(resizedImage));
		
	}
