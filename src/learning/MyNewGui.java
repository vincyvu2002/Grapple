    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
		MyPass1 = new String(ThisIsMyPassword1.getPassword());
		MyPass2 = new String(ThisIsMyPassword2.getPassword());
		MyHint = new String(ThisIsMyHint.getText());
		if (MyPass1.equals(MyPass2) && !MyHint.equals("")) {
			remove(PasswordSetup);
			add(loginScreen);
			validate();
			repaint();
		} else {
			if (MyHint.equals("")) {
				JOptionPane.showMessageDialog(this, "You have no password hint", "Password hint warning", JOptionPane.WARNING_MESSAGE);
			} else {
				JOptionPane.showMessageDialog(this, "The passwords do not match", "Password match warning", JOptionPane.WARNING_MESSAGE);
			}
		}
    }//GEN-LAST:event_jButton1ActionPerformed

